package introduction_to_java.first_program;

public class PrimitiveDatatype {
	public static void main(String[] args) {
		//PrimitiveDataTypes
		
				/*NUMERIC DATATYPES
				 * 1.byte =23(default=0)
				 * 2.short =300((default=0)
				 * 3.integer= 3000(default=0)
				 * long =300L(default=0L)
				 * 5.float=8.9f(default=0.0f)
				 * 6.double=9.1(default=0.0)
				 * 7.char='a'(default='')
				 * 8. boolean=true(default=false)
				 */
			//declare variables
				/*
				 * variable - it is container used to store a values based on the data type
				 * <datatype> variable_name;
				 * 
				 */
				byte age=20;
				//variable name=value;
				
				//<datatype> variable name=value;
				
				byte age1 =30;
				System.out.println(age);
				System.out.println(age1);
				
				//2.Short
				
				 short myShort = 300;
			        System.out.println("Short value: " + myShort);
			        
			     //3.integer
			        int myInt = 3000;
			        System.out.println("Integer value: " + myInt);
				
			      //4.long=300L
			        long myLong=300L;
			        System.out.println("Long value:" + myLong);
			        
			      //5 float
			        float myFloat=8.9f;
			        System.out.println("Float value:"  +myFloat);
			        
			      //6 double=9.1
			        double myDouble=9.1;
			        System.out.println("Double value:" +myDouble);
			        
			      //7 char='a'
			        char myChar='a';
			        System.out.println("Char value:" +myChar);
			        
			      //8 boolean=true
			        boolean myBoolean=true;
			        System.out.println("Boolean value:" +myBoolean);
			        
			        	
		
		
	}

}
